export type TranslationKey = keyof typeof translations.en;

export function interpolate(
  text: string,
  values: Record<string, string | number> = {},
) {
  return text.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}
